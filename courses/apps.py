"""
Конфигурация приложения courses.

Этот файл определяет метаданные для приложения Django.
"""

from django.apps import AppConfig


class CoursesConfig(AppConfig):
    """
    Класс конфигурации приложения.

    Django использует этот класс для настройки приложения
    и его интеграции с проектом.
    """

    # Тип поля для автоматических первичных ключей
    default_auto_field = 'django.db.models.BigAutoField'

    # Имя приложения (должно совпадать с именем папки)
    name = 'courses'

    # Человеко-читаемое название приложения (для админки)
    verbose_name = 'Управление курсами'